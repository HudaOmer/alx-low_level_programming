#include "operations.h"

/**
 * add - addition
 * @a: addend
 * @b: addend
 * Return: Sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtraction
 * @a: minuend
 * @b: subtrahend
 * Return: Differance
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multipication
 * @a: multiplicand
 * @b: multiplier
 * Return: Product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division
 * @a: num
 * @b: divider
 * Return: Product
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus
 * @a: num
 * @b: divider
 * Return: mod
 */
int mod(int a, int b)
{
	return (a % b);
}
